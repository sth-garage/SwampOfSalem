    /// <summary>
    /// Returns true for moods where the gator should resist changing their vote target.
    /// </summary>
    public static bool IsPersuasionImmune(Mood mood) =>
        mood is Mood.Convinced or Mood.Obsessed or Mood.Bonded or Mood.LastStand;

    /// <summary>
    /// Returns true for moods where the gator should not initiate conversations.
    /// </summary>
    public static bool AvoidsSocialising(Mood mood) =>
        mood is Mood.Stonewalling or Mood.Submerged or Mood.Territorial or Mood.Resigned or Mood.Isolated;
